public class Operations {
    public static void addition(int num1, int num2){
        int adição = num1 + num2;
        System.out.println("Addition: "+num1+"+"+num2+"="+adição+"\n");
    }

    public static void subtration(int num1, int num2){
        int subtration = num1 - num2;
        System.out.println("Subtration: "+num1+"-"+num2+"="+subtration+"\n");
    }

    public static void division(int num1, int num2){
        int division = num1/num2;
        System.out.println("Division: "+num1+"/"+num2+"="+division+"\n");
    }

    public static void multiplication(int num1, int num2){
        int multplication = num1*num2;
        System.out.println("Multplication: "+num1+"*"+num2+"="+multplication+"\n");
    }
}
